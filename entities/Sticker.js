class Sticker {

    id;
    name;
    picture;
    cost;


    getId(){
        return this.id;
    }

    setId(id){
        this.id = id;
    }

    getName(){
        return this.name;
    }

    setName(name){
        this.name = name;
    }

    getPicture(){
        return this.picture;
    }

    setPicture(picture){
        this.picture = picture;
    }

    getCost(){
        return this.cost;
    }

    setCost(cost){
        this.cost = cost;
    }
}