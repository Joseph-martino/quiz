class Sticker {

    name;
    picture;
    cost;

    getName(){
        return this.name;
    }

    setName(name){
        this.name = name;
    }

    getPicture(){
        return this.picture;
    }

    setPicture(picture){
        this.picture = picture;
    }

    getCost(){
        return this.cost;
    }

    setCost(cost){
        this.cost = cost;
    }
}